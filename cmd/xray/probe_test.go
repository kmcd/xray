func TestNormalizeSlug(t *testing.T) {
	cases := []struct {
		in   string
		want string
	}{
		{"dealers-site", "dealers_site"},
		{"dealers_site", "dealers_site"},
		{"Dealers Site", "dealers_site"},
		{"admin-js", "admin"},
		{"api-ruby", "api"},
		{"backend-python", "backend"},
		{"foo.bar", "foo_bar"},
		{"MyRepo", "myrepo"},
	}
	for _, c := range cases {
		if got := normalizeSlug(c.in); got != c.want {
			t.Errorf("normalizeSlug(%q) = %q, want %q", c.in, got, c.want)
		}
	}
}

func TestMatchRepo(t *testing.T) {
	repos := []string{
		"acme/dealers_site",
		"acme/admin_site",
		"acme/api",
	}
	cases := []struct {
		projectName string
		wantSlug    string
		wantConf    string
	}{
		{"dealers-site", "acme/dealers_site", "high"},
		{"dealers_site", "acme/dealers_site", "high"},
		{"Dealers Site", "acme/dealers_site", "high"},
		{"admin-site", "acme/admin_site", "high"},
		// _js suffix stripped by normalizeSlug → exact match
		{"dealers-site-js", "acme/dealers_site", "high"},
		// no match
		{"notaproject", "", ""},
	}
	for _, c := range cases {
		gotSlug, gotConf := matchRepo(c.projectName, repos)
		if gotSlug != c.wantSlug || gotConf != c.wantConf {
			t.Errorf("matchRepo(%q) = (%q, %q), want (%q, %q)",
				c.projectName, gotSlug, gotConf, c.wantSlug, c.wantConf)
		}
	}
}

