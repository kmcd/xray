func TestRenderProbeDraft_ContainsExpectedSections(t *testing.T) {
	r := probeResult{
		Org: "acme",
		GitHub: ghProbe{
			ActiveRepos: []string{"acme/api", "acme/web"},
		},
		CircleCI: cciProbe{
			Followed: []cciFollowed{
				{CCISlug: "gh/acme/api", RepoSlug: "acme/api"},
			},
		},
		Bugsnag: bnProbe{
			OrgName:  "Acme Inc",
			Projects: []bnProject{{ID: "abc123", Name: "api-backend"}},
			Matches:  []bnMatch{{Project: bnProject{ID: "abc123", Name: "api-backend"}, RepoSlug: "acme/api", Confidence: "high"}},
		},
		Honeycomb: hcProbe{
			Datasets:    []hcDataset{{Name: "production", Slug: "production"}},
			Attribution: []hcAttr{{Dataset: "production", RepoSlug: "acme/api", MarkerCount: 100}},
		},
		Sentry: sentryProbe{
			OrgSlug:  "acme-sentry",
			Projects: []sentryProject{{Slug: "web-app", Name: "Web App"}},
			Matches:  []sentryMatch{{Project: sentryProject{Slug: "web-app", Name: "Web App"}, RepoSlug: "acme/web", Confidence: "medium"}},
		},
	}

	body, err := renderProbeDraft(r)
	if err != nil {
		t.Fatalf("renderProbeDraft: %v", err)
	}

	checks := []string{
		"generated by xray init --from-org acme --probe",
		`"acme/api"`,
		`"acme/web"`,
		`"gh/acme/api" = "acme/api"`,        // CircleCI mapping
		`"abc123" = "acme/api"`,              // Bugsnag mapping
		`dataset = "production"`,             // Honeycomb recommended
		`"web-app" = "acme/web"`,             // Sentry mapping
		`organization = "acme-sentry"`,
	}
	for _, want := range checks {
		if !strings.Contains(body, want) {
			t.Errorf("renderProbeDraft missing %q\nfull output:\n%s", want, body)
		}
	}
}

