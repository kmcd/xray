func TestInitProbe_RefusesOverwrite(t *testing.T) {
	payload := `[{"name": "api", "full_name": "acme/api", "archived": false}]`
	withFakeGitHub(t, payload)

	outPath := filepath.Join(t.TempDir(), "xray.toml.draft")
	if err := os.WriteFile(outPath, []byte("existing"), 0o600); err != nil {
		t.Fatal(err)
	}

	// --probe without --force should refuse to overwrite.
	cciSrv := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, _ *http.Request) {
		w.Header().Set("Content-Type", "application/json")
		_, _ = w.Write([]byte("[]"))
	}))
	defer cciSrv.Close()
	orig := probeCCIBaseURL
	probeCCIBaseURL = cciSrv.URL
	defer func() { probeCCIBaseURL = orig }()

	stub401 := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, _ *http.Request) {
		http.Error(w, "unauthorized", http.StatusUnauthorized)
	}))
	defer stub401.Close()
	for _, p := range []*string{&probeBugsnagBaseURL, &probeHoneycombBaseURL, &probeSentryBaseURL} {
		orig := *p
		*p = stub401.URL
		defer func(pp *string, o string) { *pp = o }(p, orig)
	}

	t.Setenv("CIRCLECI_TOKEN", "tok")
	t.Setenv("BUGSNAG_AUTH_TOKEN", "")
	t.Setenv("HONEYCOMB_API_KEY", "")
	t.Setenv("HC_API_KEY", "")
	t.Setenv("SENTRY_AUTH_TOKEN", "")

	root, _, _ := newTestRoot(t)
	root.SetArgs([]string{"init", "--from-org", "acme", "--token", "tok", "--probe", "--out", outPath})
	err := root.Execute()
	if err == nil {
		t.Fatal("expected error refusing to overwrite")
	}
	if !strings.Contains(err.Error(), "already exists") {
		t.Errorf("err = %v, want 'already exists'", err)
	}
	got, _ := os.ReadFile(outPath)
	if string(got) != "existing" {
		t.Error("file was modified without --force")
	}
}
