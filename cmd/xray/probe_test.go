func TestProbeSentry_MatchesAndPaginates(t *testing.T) {
	page2URL := ""
	mux := http.NewServeMux()
	mux.HandleFunc("/projects/", func(w http.ResponseWriter, r *http.Request) {
		w.Header().Set("Content-Type", "application/json")
		if r.URL.RawQuery == "cursor=1" {
			// second page — no next link
			_ = json.NewEncoder(w).Encode([]map[string]any{
				{"slug": "backend", "name": "Backend", "organization": map[string]any{"slug": "acme-sentry"}},
			})
			return
		}
		// first page — includes next link pointing at cursor=1
		page2URL = "http://" + r.Host + "/projects/?cursor=1"
		w.Header().Set("Link", `<`+page2URL+`>; rel="next"; results="true"`)
		_ = json.NewEncoder(w).Encode([]map[string]any{
			{"slug": "frontend", "name": "Frontend", "organization": map[string]any{"slug": "acme-sentry"}},
		})
	})
	srv := httptest.NewServer(mux)
	defer srv.Close()

	orig := probeSentryBaseURL
	probeSentryBaseURL = srv.URL
	defer func() { probeSentryBaseURL = orig }()

	repos := []string{"acme/frontend", "acme/api"}
	result := probeSentry(t.Context(), srv.Client(), "tok", repos)
	if result.Skipped {
		t.Fatalf("unexpected skip: %s", result.SkipReason)
	}
	if len(result.Projects) != 2 {
		t.Fatalf("len(Projects) = %d, want 2 (pagination should collect both pages)", len(result.Projects))
	}
	if result.OrgSlug != "acme-sentry" {
		t.Errorf("OrgSlug = %q, want %q", result.OrgSlug, "acme-sentry")
	}
	// "frontend" matches "acme/frontend" — high confidence
	if len(result.Matches) != 1 || result.Matches[0].Project.Slug != "frontend" {
		t.Errorf("Matches = %+v", result.Matches)
	}
}

